#!/usr/bin/env python3
"""
This module defines a function 'add' that takes two floats and returns their sum.
"""

from typing import float


def add(a: float, b: float) -> float:
  """Adds two floats and returns their sum.

  Args:
      a: The first float number.
      b: The second float number.

  Returns:
      The sum of a and b.
  """
  return a + b
