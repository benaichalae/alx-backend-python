#!/usr/bin/env python3
"""
This module defines a function 'floor' that takes
a float and returns its floor.
"""


def floor(n: float) -> int:
    """
    Calculates the floor of a float (largest integer less than or equal to n).

    Args:
        n: The float number.

    Returns:
        The floor of n (integer less than or equal to n).
    """
    return int(n)
