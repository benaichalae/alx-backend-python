#!/usr/bin/env python3
"""
This module defines a function 'concat' that
takes two strings and returns their concatenation.
"""

from typing import str


def concat(str1: str, str2: str) -> str:
    """Concatenates two strings and returns the combined string.

    Args:
        str1: The first string.
        str2: The second string.

    Returns:
        The concatenation of str1 and str2.
    """
    return str1 + str2
